package Practice;

import java.util.Scanner;

/**
 *
 * @author mankank
 */
public class MaximumValueOfArrI_i_Arrj_j {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int N = sc.nextInt();
        int[] arr = new int[N];
        for (int i = 0; i < N; i++) {
            arr[i] = sc.nextInt();
            arr[i] -= i;
        }
        int max = 0, min = Integer.MAX_VALUE;
        for (int i = 0; i < N; i++) {
            if (arr[i] > max) {
                max = arr[i];
            }
            if (arr[i] < min) {
                min = arr[i];
            }
        }
        System.out.println(max - min);
    }
}
